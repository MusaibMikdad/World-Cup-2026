export const getTeam = (code) => {
  if (!code) return { name: 'TBD', code: 'TBD', flag: '🏳️', group: '?', confederation: '?' };
  return teams[code] || { name: code, code, flag: '🏳️', group: '?', confederation: '?' };
};

export const getGroupTeams = (groupLetter) => {
  return (groups[groupLetter] || []).map(code => getTeam(code));
};

export const calculateGroupStandings = (groupLetter, matchesList) => {
  const groupTeams = getGroupTeams(groupLetter);
  const standings = groupTeams.map(team => ({
    team: team.code,
    p: 0, w: 0, d: 0, l: 0,
    gf: 0, ga: 0, gd: 0, pts: 0
  }));

  const groupMatches = matchesList.filter(m => m.group === groupLetter);

  groupMatches.forEach(m => {
    const hasScore = m.score && m.score.home !== null && m.score.away !== null;
    const isPlayed = m.status === 'FINISHED' || m.status === 'LIVE';
    if (isPlayed && hasScore) {
      const homeRow = standings.find(r => r.team === m.home);
      const awayRow = standings.find(r => r.team === m.away);

      if (homeRow && awayRow) {
        homeRow.p += 1;
        awayRow.p += 1;

        const hs = m.score.home;
        const as = m.score.away;

        homeRow.gf += hs;
        homeRow.ga += as;
        awayRow.gf += as;
        awayRow.ga += hs;

        if (hs > as) {
          homeRow.w += 1;
          homeRow.pts += 3;
          awayRow.l += 1;
        } else if (hs < as) {
          awayRow.w += 1;
          awayRow.pts += 3;
          homeRow.l += 1;
        } else {
          homeRow.d += 1;
          homeRow.pts += 1;
          awayRow.d += 1;
          awayRow.pts += 1;
        }
      }
    }
  });

  standings.forEach(r => {
    r.gd = r.gf - r.ga;
  });

  standings.sort((a, b) => {
    if (b.pts !== a.pts) return b.pts - a.pts;
    if (b.gd !== a.gd) return b.gd - a.gd;
    if (b.gf !== a.gf) return b.gf - a.gf;
    return a.team.localeCompare(b.team);
  });

  return standings;
};
